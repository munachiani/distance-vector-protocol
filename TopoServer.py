

class TopoServer:
    def __init__(self, id, ip, port, c):
        self.id = id
        self.ip = ip
        self.port = port
        self.cost = c


        def getIp(self): return self.ip
        def getId(self): return self.id
        def getPort(self): return self.port
        def getCost(self): return self.cost



        
